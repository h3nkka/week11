        {
            return da.Sum();
        }

        public static double Average(double[] da)
        {
            return da.Average();
        }

        public static double Min(double[] da)
        {
            return da.Min();
        }

        public static double Max(double[] da)
        {
            return da.Max();
        }
    }
}
